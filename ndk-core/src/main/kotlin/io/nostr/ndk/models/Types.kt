package io.nostr.ndk.models

/**
 * Type alias for Nostr event IDs (32-byte hex string)
 */
typealias EventId = String

/**
 * Type alias for Nostr public keys (32-byte hex string)
 */
typealias PublicKey = String

/**
 * Type alias for Schnorr signatures (64-byte hex string)
 */
typealias Signature = String

/**
 * Type alias for Unix timestamps in seconds
 */
typealias Timestamp = Long
